package com.example.ourmemory.model;

public class ContactUsDTO {
    private int contactUs_num;
    private String contactUs_subject;
    private String contactUs_content;
    private String contactUs_logtime;

    public int getContactUs_num() {
        return contactUs_num;
    }

    public void setContactUs_num(int contactUs_num) {
        this.contactUs_num = contactUs_num;
    }

    public String getContactUs_subject() {
        return contactUs_subject;
    }

    public void setContactUs_subject(String contactUs_subject) {
        this.contactUs_subject = contactUs_subject;
    }

    public String getContactUs_content() {
        return contactUs_content;
    }

    public void setContactUs_content(String contactUs_content) {
        this.contactUs_content = contactUs_content;
    }

    public String getContactUs_logtime() {
        return contactUs_logtime;
    }

    public void setContactUs_logtime(String contactUs_logtime) {
        this.contactUs_logtime = contactUs_logtime;
    }
}
